package com.vertacnik.inmobiliaria.modelo;

import java.io.Serializable;

//Inquilino que viene embebido dentro del Contrato (api/Contratos)
public class Inquilino implements Serializable {
    private int idInquilino;
    private String nombre, apellido, dni, telefono, email;

    public Inquilino() {}

    public int getIdInquilino() { return idInquilino; }
    public void setIdInquilino(int idInquilino) { this.idInquilino = idInquilino; }

    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }

    public String getApellido() { return apellido; }
    public void setApellido(String apellido) { this.apellido = apellido; }

    public String getDni() { return dni; }
    public void setDni(String dni) { this.dni = dni; }

    public String getTelefono() { return telefono; }
    public void setTelefono(String telefono) { this.telefono = telefono; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }
}
